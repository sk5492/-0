# 파이션 산술 연산자 종류
a = int(input("첫 번째 수 입력 : "))
b = int(input("두 번째 수 입력 : "))

print(a, '+', b, '=', (a+b))        #더허기
print(a, '-', b, '=', (a-b))        #빼기
print(a, '*', b, '=', (a*b))        #곱하기
print(a, '/', b, '=', (a/b))        #나누기
print(a, '%', b, '=', (a%b))        #나머지
print(a, '**', b, '=', (a**b))        #제곱
print(a, '//', b, '=', (a//b))        #몫
